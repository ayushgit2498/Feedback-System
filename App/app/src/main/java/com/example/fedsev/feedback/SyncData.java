package com.example.fedsev.feedback;

import android.arch.persistence.room.Entity;
import android.arch.persistence.room.PrimaryKey;
import android.support.annotation.NonNull;

@Entity
public class SyncData {

    String cust_id;
    String first_name;
    String last_name;
    String phone;
    String vehical_number;
    String model;
    @NonNull
    @PrimaryKey
    String service_id;
    String l_service;
    String n_service;
    int kms;

    int callisdone;

    public int getCallisdone() {
        return callisdone;
    }

    public void setCallisdone(int callisdone) {
        this.callisdone = callisdone;
    }

    public String getCust_id() {
        return cust_id;
    }

    public void setCust_id(String cust_id) {
        this.cust_id = cust_id;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getVehical_number() {
        return vehical_number;
    }

    public void setVehical_number(String vehical_number) {
        this.vehical_number = vehical_number;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getService_id() {
        return service_id;
    }

    public void setService_id(String service_id) {
        this.service_id = service_id;
    }

    public String getL_service() {
        return l_service;
    }

    public void setL_service(String l_service) {
        this.l_service = l_service;
    }

    public String getN_service() {
        return n_service;
    }

    public void setN_service(String n_service) {
        this.n_service = n_service;
    }

    public int getKms() {
        return kms;
    }

    public void setKms(int kms) {
        this.kms = kms;
    }
}
